package fundamentos;

public class AreaCircunferencia {
	
	public static void main(String[] args) {
		//numeros reais
		double raio = 3.4;
		//numeros inteiros
		int teste = 1;
		// o final faz eu colocar a variavel inalteravel tornando-se uma constante   
		//lembrando que precisa escrever constante com letra maiuscula
		final double PI = 3.14159;
		double area = PI * raio * raio;
				
		System.out.println("MINHA AREA E: " + area + " M2");
		System.out.println(teste);
		
	}

}
